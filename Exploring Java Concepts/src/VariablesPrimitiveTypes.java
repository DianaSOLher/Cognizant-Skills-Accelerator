public class VariablesPrimitiveTypes {
    public static void main(String[] args) {
            int age = 24;
            double height = 62.99;
            char initial = 'V';
            boolean isStudent = false;

            System.out.println(age);
            System.out.println(height);
            System.out.println(initial);
            System.out.println(isStudent);
    }
}
